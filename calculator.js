//addition calculator
function add(num1,num2){
  return num1 + num2;
}

//function subtract
function subtract(num1,num2){
  return num1-num2
}

//function multiply
function multiply(num1,num2){
  return num1*num2;
}

//function divide
function divide(num1,num2){
  return num1/num2;
}

//output

console.log(add(55,55))
console.log(subtract(77,333))
console.log(multiply(66,33))
console.log(divide(1,2))

